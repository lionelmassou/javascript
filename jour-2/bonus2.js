var month = "january";

switch (month) {
    case "december":
    case "january":
    case "february":
        console.log("Winter");
        break;
    case "march":
    case "japril":
    case "may":
        console.log("Spring");
        break;
    case "june":
    case "july":
    case "august":
        console.log("Summer");
        break;
    case "september":
    case "october":
    case "november":
        console.log("Fall");
        break;
    default:
        console.log("That's not a Month");
}